void USART1_IRQHandler(void)
{

  if(USART_GetITStatus(Port_USART, USART_IT_RXNE) != RESET)
  { 
  	USART_ClearITPendingBit(USART1,USART_IT_RXNE);
		uartDataBytes[uiCountNumBytesRecv++]=USART_ReceiveData(Port_USART);
		ucReceiveStatus=1;
  }
	
}
