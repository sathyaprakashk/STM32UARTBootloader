void UART4_IRQHandler(void)
{
  if(USART_GetITStatus(UART4, USART_IT_RXNE) != RESET)
  { 
  		USART_ClearITPendingBit(UART4,USART_IT_RXNE);
		uart4Received = 1;
  }
}
